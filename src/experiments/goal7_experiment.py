"""
Goal 7: Compute and plot the probability density functions (PDFs) of the
population activity signals flowing into the skeleton.

Naming convention (professor):
  X = Sea,       X(t) = number of sea nodes firing at time t
  Y = Inter,     Y(t) = number of inter-nodes firing at time t
  Z = Skeleton   (not used here)

X(t) and Y(t) are computed by summing all binary firing states across the
respective population at each timestep. The PDFs p(X) and p(Y) are then
plotted as normalized histograms (area = 1).

Requires: data/goal7/states_100k.npy  (generated by this script on first run)
          or reuses existing file if already present.

Usage (from project root):
    python src/experiments/goal7_experiment.py
"""

import sys
from pathlib import Path
sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

import numpy as np
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt

from weights.prebeck_125node import build_prebeck_125node_weight_matrix
from core.dynamics_prebeck   import PrebeckBoltzmannDynamics
from core.simulate_states    import run_recorded_states
from utils.io_paths          import ensure_dir

# ── Configuration ─────────────────────────────────────────────────────────────
NS      = 10
DS      = 0.35
SEED    = 42
BURN_IN = 10_000
T       = 100_000
OUT_DIR = Path('data/goal7')
ensure_dir(OUT_DIR)

STATES_FILE = OUT_DIR / 'states_100k.npy'
IDX_FILE    = OUT_DIR / 'idx_100k.npy'
T0_FILE     = OUT_DIR / 't0_100k.npy'

# ── Simulate or load ──────────────────────────────────────────────────────────
if STATES_FILE.exists():
    print("Loading existing states...")
    states = np.load(STATES_FILE)
    idx    = np.load(IDX_FILE, allow_pickle=True).item()
    t0     = float(np.load(T0_FILE))
else:
    print(f"Running simulation: T={T}, ns={NS}, ds={DS:.0%}...")
    rng = np.random.default_rng(SEED)
    W, idx, t0 = build_prebeck_125node_weight_matrix(
        rng=rng, ns=NS, ds=DS, wi_mode='-t0'
    )
    nt  = W.shape[0]
    dyn = PrebeckBoltzmannDynamics(t0=t0, mask=np.ones(nt, dtype=bool))
    states = run_recorded_states(
        W=W, dynamics=dyn, T=T, burn_in=BURN_IN,
        sigma=0.0, noise_mode='different_input',
        seed=SEED, record_idx=np.arange(nt)
    )
    np.save(STATES_FILE, states)
    np.save(IDX_FILE,    idx, allow_pickle=True)
    np.save(T0_FILE,     np.array(t0))
    print("Saved.")

sk  = idx['skeleton']
sea = idx['sea']
inh = idx['inhibitory']

print(f"States shape: {states.shape}")
print(f"Fire rates — sk:{states[:,sk].mean():.3f}  "
      f"sea:{states[:,sea].mean():.3f}  inh:{states[:,inh].mean():.3f}")

# ── Compute population activity signals ───────────────────────────────────────
# X(t): how many sea nodes are firing at timestep t  (range 0..90)
# Y(t): how many inter-nodes are firing at timestep t (range 0..25)
X = states[:, sea].sum(axis=1).astype(float)   # (T,)
Y = states[:, inh].sum(axis=1).astype(float)   # (T,)

print(f"\nX(t): mean={X.mean():.2f}  std={X.std():.2f}  "
      f"min={X.min():.0f}  max={X.max():.0f}")
print(f"Y(t): mean={Y.mean():.2f}  std={Y.std():.2f}  "
      f"min={Y.min():.0f}  max={Y.max():.0f}")

np.save(OUT_DIR / 'X_sea_activity.npy',   X)
np.save(OUT_DIR / 'Y_inter_activity.npy', Y)

# ── Plot ──────────────────────────────────────────────────────────────────────
fig, axes = plt.subplots(1, 2, figsize=(13, 5))
fig.suptitle(
    'Goal 7 — Probability Density Functions of Population Activity\n'
    f'Full Prebeck 125-node Network  |  ds={DS:.0%},  wᵢ=−t₀,  σ=0',
    fontsize=13, fontweight='bold'
)

# p(X) — sea population
ax = axes[0]
ax.hist(X, bins=np.arange(-0.5, len(sea) + 1.5, 1), density=True,
        color='#2166ac', edgecolor='white', linewidth=0.4, alpha=0.85)
ax.axvline(X.mean(), color='#d6604d', linewidth=2, linestyle='--',
           label=f'mean = {X.mean():.1f}')
ax.set_xlabel('X(t)  —  number of sea nodes firing at time t', fontsize=10)
ax.set_ylabel('Probability density  p(X)', fontsize=10)
ax.set_title('p(X)  —  Sea population activity', fontsize=11, fontweight='bold')
ax.set_xlim(-1, len(sea) + 1)
ax.legend(fontsize=9)
ax.grid(True, alpha=0.3)

# p(Y) — inter population
ax = axes[1]
ax.hist(Y, bins=np.arange(-0.5, len(inh) + 1.5, 1), density=True,
        color='#d6604d', edgecolor='white', linewidth=0.4, alpha=0.85)
ax.axvline(Y.mean(), color='#2166ac', linewidth=2, linestyle='--',
           label=f'mean = {Y.mean():.1f}')
ax.set_xlabel('Y(t)  —  number of inter-nodes firing at time t', fontsize=10)
ax.set_ylabel('Probability density  p(Y)', fontsize=10)
ax.set_title('p(Y)  —  Inter-node population activity', fontsize=11, fontweight='bold')
ax.set_xlim(-1, len(inh) + 1)
ax.legend(fontsize=9)
ax.grid(True, alpha=0.3)

plt.tight_layout()
out_path = OUT_DIR / 'goal7_pdfs.png'
plt.savefig(out_path, dpi=180, bbox_inches='tight')
plt.close(fig)
print(f"\nPlot saved to {out_path}")

# ── Binary PDF plot (professor's literal interpretation) ──────────────────────
# Flatten all individual Xi(t) and Yi(t) values across all neurons and timesteps
X_bin = states[:, sea].flatten()   # 90 * T values, each 0 or 1
Y_bin = states[:, inh].flatten()   # 25 * T values, each 0 or 1

print(f"\nBinary PDFs:")
print(f"X: p(0)={(X_bin==0).mean():.4f}  p(1)={(X_bin==1).mean():.4f}")
print(f"Y: p(0)={(Y_bin==0).mean():.4f}  p(1)={(Y_bin==1).mean():.4f}")

fig2, axes2 = plt.subplots(1, 2, figsize=(10, 5))
fig2.suptitle(
    'Goal 7 — Binary PDFs  p(X) and p(Y)\n'
    'All individual neuron activations Xᵢ(t) and Yᵢ(t)  |  '
    f'ds={DS:.0%},  wᵢ=−t₀,  σ=0',
    fontsize=12, fontweight='bold'
)

for ax, data, label, color, name in [
    (axes2[0], X_bin, 'X  (sea neuron activation)',   '#2166ac', 'p(X)  —  Sea'),
    (axes2[1], Y_bin, 'Y  (inter-node activation)',   '#d6604d', 'p(Y)  —  Inter-nodes'),
]:
    counts = np.bincount(data, minlength=2) / len(data)
    ax.bar([0, 1], counts, color=color, edgecolor='white', width=0.4, alpha=0.85)
    ax.set_xticks([0, 1])
    ax.set_xticklabels(['0  (silent)', '1  (firing)'], fontsize=10)
    ax.set_xlabel(label, fontsize=10)
    ax.set_ylabel('Probability  p', fontsize=10)
    ax.set_title(name, fontsize=11, fontweight='bold')
    ax.set_ylim(0, 1.0)
    for i, v in enumerate(counts):
        ax.text(i, v + 0.02, f'{v:.3f}', ha='center', fontsize=11, fontweight='bold')
    ax.grid(True, axis='y', alpha=0.3)

plt.tight_layout()
out_path2 = OUT_DIR / 'goal7_binary_pdfs.png'
plt.savefig(out_path2, dpi=180, bbox_inches='tight')
plt.close(fig2)
print(f"Binary PDF plot saved to {out_path2}")

# ── Logistic probability distributions ───────────────────────────────────────
# Record sigmoid(z_i(t)/t0) for each sea and inter node at each timestep
# This requires running the simulation manually to capture pre-sampling probs

print("\nRecording logistic probabilities (requires separate manual sim loop)...")
rng_log = np.random.default_rng(SEED)

# Rebuild W for the logistic recording run
rng2 = np.random.default_rng(SEED)
W2, idx2, t02 = build_prebeck_125node_weight_matrix(
    rng=rng2, ns=NS, ds=DS, wi_mode='-t0'
)
nt2 = W2.shape[0]
s2  = rng_log.integers(0, 2, size=nt2, dtype=np.uint8)

# Burn in
for _ in range(BURN_IN):
    z2 = s2.astype(float) @ W2 / t02
    p2 = 1.0 / (1.0 + np.exp(-z2))
    s2 = (rng_log.random(nt2) < p2).astype(np.uint8)

# Record
p_sea_log   = np.zeros((T, len(sea)),  dtype=np.float32)
p_inter_log = np.zeros((T, len(inh)), dtype=np.float32)

for t_i in range(T):
    z2 = s2.astype(float) @ W2 / t02
    p2 = 1.0 / (1.0 + np.exp(-z2))
    p_sea_log[t_i]   = p2[sea]
    p_inter_log[t_i] = p2[inh]
    s2 = (rng_log.random(nt2) < p2).astype(np.uint8)

np.save(OUT_DIR / 'p_sea_logistic.npy',   p_sea_log)
np.save(OUT_DIR / 'p_inter_logistic.npy', p_inter_log)

print(f"p_sea:   mean={p_sea_log.mean():.3f}  std={p_sea_log.std():.3f}")
print(f"p_inter: mean={p_inter_log.mean():.3f}  std={p_inter_log.std():.3f}")

fig3, axes3 = plt.subplots(1, 2, figsize=(13, 5))
fig3.suptitle(
    'Goal 7 — Distribution of Logistic Activation Probabilities\n'
    f'Full Prebeck 125-node Network  |  ds={DS:.0%},  wᵢ=−t₀,  σ=0',
    fontsize=13, fontweight='bold'
)

for ax, p_vals, color, name, label in [
    (axes3[0], p_sea_log.flatten(),   '#2166ac',
     'p(pₓ)  —  Sea',         'pₓ  =  sigmoid(zᵢ/t₀)  for sea nodes'),
    (axes3[1], p_inter_log.flatten(), '#d6604d',
     'p(p_y)  —  Inter-nodes', 'p_y  =  sigmoid(zᵢ/t₀)  for inter-nodes'),
]:
    ax.hist(p_vals, bins=100, density=True,
            color=color, edgecolor='none', alpha=0.85)
    ax.axvline(p_vals.mean(), color='black', linewidth=1.8, linestyle='--',
               label=f'mean = {p_vals.mean():.3f}')
    ax.set_xlabel(label, fontsize=10)
    ax.set_ylabel('Probability density', fontsize=10)
    ax.set_title(name, fontsize=11, fontweight='bold')
    ax.set_xlim(0, 1)
    ax.legend(fontsize=9)
    ax.grid(True, alpha=0.3)

plt.tight_layout()
out_path3 = OUT_DIR / 'goal7_logistic_pdfs.png'
plt.savefig(out_path3, dpi=180, bbox_inches='tight')
plt.close(fig3)
print(f"Logistic PDF plot saved to {out_path3}")
